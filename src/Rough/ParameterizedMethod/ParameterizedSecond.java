package Rough.ParameterizedMethod;

public class ParameterizedSecond {
    public static void main(String[] args) {
        System.out.println(message("Benazira", " Sadaat"));
    }

    public static String message(String mum, String jum) {
        String m = mum;
        String j = jum;
        String result =  m + j ;
        return result;
    }
}

package HomeWork.DifferentMethods;

public class Remainder {
    public static void main(String[] args) {
        /*
        I. Declare and store the following value to the variables.

        1. The first variable should have a value of 18
        2. The second variable should have a value of 5

        II. Print the remainder of the first and second variables in the console by using remainder operator.

         */

        // Declare and initialize variables
        int num1 = 18;
        int num2 = 5;

        // Calculate and print the remainder
        int remainder = num1 % num2;
        System.out.println("The remainder of " + num1 + " divided by " + num2 + " is: " + remainder);
    }
}

package Week_13_06242024.day_1.abstraction;

public class WhatIsAbstractClass {

    /*
    *
    * A class which contains abstract keyword in its declaration then it is an Abstract Class.
    *
    * Points to remember:

    • Abstract classes may or may not include abstract methods

    • If a class is declared abstract then it cannot be instantiated (create a method).

    • If a class has abstract method then we have to declare the class as abstract class.

    • When an abstract class is subclassed, the subclass usually provides implementations for
     all the abstract methods in its parent class.

    However, if it does not, then the subclass must also be declared abstract.
    * */


}

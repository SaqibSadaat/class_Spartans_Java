package week_8_Loop.day_1;

public class WhileLoop {
    // keyword = while
    // Syntax of while loop
    /*
     * int i = 10;
     *
     * while ( condition ) {
     *   iteration;
     * }
     * */

    public static void main(String[] args) {

        // Print the numbers from 1 to 5

        int number = 1;
        while (number <= 10) {
            System.out.print(number + " ");
            number++;
        }
    }
}

package week_7.day_3;

public class ForLoopString {
    public static void main(String[] args) {
        //Print John 15 times
        String[] names = {"John", "Doe", "Alex", "Eric", "Tek", "School",};
        for (int index = 0; index < names.length; index++ ){
            System.out.println(names[index]);
        }
    }
}
